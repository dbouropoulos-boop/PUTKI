export const getDayName = (lang) => {
  return new Intl.DateTimeFormat(lang === 'en' ? 'en-GB' : 'fi-FI', {
    timeZone: 'Europe/Helsinki',
    weekday: 'long',
  }).format(new Date());
};

export const getDayNum = () => {
  return new Intl.DateTimeFormat('fi-FI', { timeZone: 'Europe/Helsinki', day: 'numeric' }).format(new Date());
};

export const formatDate = (lang) => {
  return new Date().toLocaleDateString(lang === 'en' ? 'en-GB' : 'fi-FI');
};

export const interpolate = (str, vars = {}) => {
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
};
